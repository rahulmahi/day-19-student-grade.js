let mark = parseInt(prompt("Enter Your Mark"))

if(mark >= 90)
{
    console.log("Student is A grade");
}
else if(mark >= 80)
{
    console.log("Student is B grade");
}
else if(mark >= 60)
{
    console.log("Student is C grade");
}
else if(mark >= 50)
{
    console.log("Student is D grade")
}
else{
    console.log("Student is fail");
}